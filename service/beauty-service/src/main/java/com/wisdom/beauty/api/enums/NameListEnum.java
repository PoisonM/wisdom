package com.wisdom.beauty.api.enums;

public enum NameListEnum {

    WHITE("0", "白名单"),
    BLACK("1", "黑名单");

    NameListEnum(String code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    private String code;
    private String desc;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
